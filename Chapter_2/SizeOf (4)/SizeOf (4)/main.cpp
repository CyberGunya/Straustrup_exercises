#include <iostream>
using namespace std;

int main()
{
    cout<<"Size of char"<<" is : "<<sizeof(char)<<"\n";
    cout<<"Size of short int"<<" is : "<<sizeof(short int)<<"\n";
    cout<<"Size of int"<<" is : "<<sizeof(int)<<"\n";
    cout<<"Size of long int"<<" is : "<<sizeof(long int)<<"\n";
    cout<<"Size of char*"<<" is : "<<sizeof(char*)<<"\n";
    cout<<"Size of short int*"<<" is : "<<sizeof(short int*)<<"\n";
    cout<<"Size of int*"<<" is : "<<sizeof(int*)<<"\n";
    cout<<"Size of long int*"<<" is : "<<sizeof(long int*)<<"\n";
    return 0;
}
